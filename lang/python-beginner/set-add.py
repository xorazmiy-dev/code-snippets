# To’plam yaratish va unga o’zgaruvchi kiritish
set1 = {1, 2, 3}

# Xuddi shu to’plamga havola qiladigan boshqa bir to’plam yaratish
set2 = set1

# To’plamni bitta havola orqali o’zgartirish
set1.add(4)

# To'plamlar ichini tekshirish
print(set1)  # Chiqish: {1, 2, 3, 4}
print(set2)  # Chiqish: {1, 2, 3, 4}