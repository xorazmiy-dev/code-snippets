# int dan floatga aniq konvertatsiya qilish

int_number = 42
float_number = float(int_number)
print(float_number) # Output: 42.0

# float dan int ga aniq konvertatsiya

float_number = 3.14
int_number = int(float_number)
print(int_number) # Output: 3


