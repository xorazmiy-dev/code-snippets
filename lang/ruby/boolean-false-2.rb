# 2.5 soni 1.5 sonidan katta, demak 2.5 > 1.5 = true
# va "!" opertori bilan birga !true - false chop etadi
puts !(2.5 > 1.5)


# 5 soni 4 sonidan katta, demak 5 > 4 = true
# 4 soni 5 sonidan katta emas, demak 4 > 5 = false
# true && false => flase - natijasini beradi
# va "!" opertori bilan birga !false - true chop etadi
puts !(5 > 4 && 4 > 5)
