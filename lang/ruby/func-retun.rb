# quyidagi metod 2 ta parametr qabul qiladi
def yig’indi(son_1, son_2)
  # ikki sonning yig'indisini qaytaradi
  return son_1 + son_2
end


# natija = 12
natija = yig’indi(5, 7)


# natijani chop etish
puts natija # 12


# yoki metod qaytargan natijani to'g'ridan - to'g'ri chop etish
puts yig’indi(6, 8) # 14
